assert.strictEqual(isRequestIntent('Rekvizitlarni tashlab bering'), false);
assert.strictEqual(isRequestIntent('Uyqurda obyekt qo‘shishni o‘rgatib yuboring'), true);
assert.strictEqual(isRequestIntent('Uyqur bo‘yicha savolim bor'), true);
assert.strictEqual(isRequestIntent('Smeta hisobotini chiqara olmayapman'), true);
